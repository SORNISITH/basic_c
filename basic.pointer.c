int sum_arr(int n, int* const arr)
{
    int* p = arr;
    int temp = 0;
    while (p < arr + n) {
        temp += *p++;
    }
    return temp;
}

bool match_number(int* const a, int n, int key)
{
    int* p = a;
    while (p < a + n) {
        if (key == *p++) {
            return true;
        }
    }
    return false;
}

